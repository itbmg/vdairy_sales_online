    void GetReport()
    {
        try
        {
            vdm = new VehicleDBMgr();
            pnlHide.Visible = true;
            DataTable Report = new DataTable();
            DateTime fromdate = DateTime.Now;
            NewReport = new DataTable();
            string[] fromdatestrig = txtFromdate.Text.Split(' ');
            if (fromdatestrig.Length > 1)
            {
                if (fromdatestrig[0].Split('-').Length > 0)
                {
                    string[] dates = fromdatestrig[0].Split('-');
                    string[] times = fromdatestrig[1].Split(':');
                    fromdate = new DateTime(int.Parse(dates[2]), int.Parse(dates[1]), int.Parse(dates[0]), int.Parse(times[0]), int.Parse(times[1]), 0);
                }
            }
            DateTime todate = DateTime.Now;
            string[] todatestrig = txtTodate.Text.Split(' ');
            if (todatestrig.Length > 1)
            {
                if (todatestrig[0].Split('-').Length > 0)
                {
                    string[] dates = todatestrig[0].Split('-');
                    string[] times = todatestrig[1].Split(':');
                    todate = new DateTime(int.Parse(dates[2]), int.Parse(dates[1]), int.Parse(dates[0]), int.Parse(times[0]), int.Parse(times[1]), 0);
                }
            }
            lbl_fromDate.Text = fromdate.ToString();
            lbl_selttodate.Text = todate.ToString();
            Session["filename"] = ddlAgentName.SelectedItem.Text + "-> Inventory Statement";
            lblAgent.Text = ddlAgentName.SelectedItem.Text; ;
            cmd = new MySqlCommand("SELECT * FROM agent_inv_bal_trans WHERE agentid=@agentid and  inv_sno=@inv_sno and inddate between @d1 and @d2");
            cmd.Parameters.AddWithValue("@d1", GetLowDate(fromdate.AddDays(-1)));
            cmd.Parameters.AddWithValue("@d2", GetHighDate(todate.AddDays(-1)));
            cmd.Parameters.AddWithValue("@inv_sno", ddlInvtory.SelectedValue);
            cmd.Parameters.AddWithValue("@agentid", ddlAgentName.SelectedValue);
            DataTable dtagenttrans = vdm.SelectQuery(cmd).Tables[0];


            if (dtagenttrans.Rows.Count > 0)
            {
                Report = new DataTable();
                Report.Columns.Add("SNo");
                Report.Columns.Add("Delivery Date");
                Report.Columns.Add("Openig");
                Report.Columns.Add("Issued", typeof(Double));
                Report.Columns.Add("Received", typeof(Double));
                Report.Columns.Add("Closing");

                int i = 1;
                foreach (DataRow dr in dtagenttrans.Rows)
                {
                    DataRow newrow = Report.NewRow();
                    newrow["SNo"] = i++.ToString();
                    string Inddate = dr["Inddate"].ToString();
                    DateTime dtIndent = Convert.ToDateTime(Inddate);
                    newrow["Delivery Date"] = dtIndent.AddDays(1).ToString();
                    newrow["Issued"] = dr["issued"].ToString();
                    newrow["Openig"] = dr["opp_balance"].ToString();
                    newrow["Received"] = dr["received"].ToString();
                    newrow["Closing"] = dr["clo_balance"].ToString();
                    Report.Rows.Add(newrow);
                }
                DataRow newvartical = Report.NewRow();
                newvartical["Delivery Date"] = "Total";
                double val = 0.0;
                foreach (DataColumn dc in Report.Columns)
                {
                    if (dc.DataType == typeof(Double))
                    {
                        val = 0.0;
                        double.TryParse(Report.Compute("sum([" + dc.ToString() + "])", "[" + dc.ToString() + "]<>'0'").ToString(), out val);
                        newvartical[dc.ToString()] = val;
                    }
                }
                Report.Rows.Add(newvartical);
                GridView grd = grdReports;
                grd.DataSource = Report;
                grd.DataBind();
                Session["xportdata"] = Report;
            }
        }
        catch (Exception ex)
        {
            lblmsg.Text = ex.Message;
            grdReports.DataSource = NewReport;
            grdReports.DataBind();
        }
    }
    protected void btn_Export_Click(object sender, EventArgs e)
    {
        try
        {
            DataTable dt = new DataTable("GridView_Data");
            foreach (TableCell cell in grdReports.HeaderRow.Cells)
            {
                dt.Columns.Add(cell.Text);
            }
            foreach (GridViewRow row in grdReports.Rows)
            {
                dt.Rows.Add();
                for (int i = 0; i < row.Cells.Count; i++)
                {
                    if (row.Cells[i].Text == "&nbsp;")
                    {
                        row.Cells[i].Text = "0";
                    }
                    dt.Rows[dt.Rows.Count - 1][i] = row.Cells[i].Text;
                }
            }
            using (XLWorkbook wb = new XLWorkbook())
            {
                wb.Worksheets.Add(dt);

                Response.Clear();
                Response.Buffer = true;
                Response.Charset = "";
                Response.ContentType = "application/vnd.openxmlformats-officedocument.spreadsheetml.sheet";
                string FileName = Session["filename"].ToString();
                Response.AddHeader("content-disposition", "attachment;filename=" + FileName + ".xlsx");
                using (MemoryStream MyMemoryStream = new MemoryStream())
                {
                    wb.SaveAs(MyMemoryStream);
                    MyMemoryStream.WriteTo(Response.OutputStream);
                    Response.Flush();
                    Response.End();
                }
            }
        }
        catch (Exception ex)
        {
            lblmsg.Text = ex.Message;
        }
    }
